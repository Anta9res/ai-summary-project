"""
提示词配置模块
管理不同版本的笔记生成提示词
"""


class PromptManager:
    """提示词管理器"""
    
    @staticmethod
    def get_prompt(version: str = "v3.0") -> str:
        """
        获取指定版本的提示词
        
        Args:
            version: 版本号(v2.0/v3.0)
            
        Returns:
            提示词文本
        """
        if version == "v3.0":
            return PromptManager._get_v3_prompt()
        elif version == "v2.0":
            return PromptManager._get_v2_prompt()
        else:
            return PromptManager._get_v3_prompt()
    
    @staticmethod
    def _get_v3_prompt() -> str:
        """v3.0提示词 - 应试化优化版"""
        return """请基于这份课件PDF，生成一份**应试导向**的复习笔记。

**核心定位**: 为期末考试服务，突出考点、题型、答题要点

## 一、重要性分级标准

### ⭐⭐⭐ 核心必考知识（重点展开）
- 基本概念定义（名词解释题）
- 核心算法原理（计算题、简答题）
- 关键公式及应用（计算题）
- 协议工作流程（填空题、简答题）
- 典型对比辨析（选择题、判断题）

### ⭐⭐ 重要知识（适度展开）
- 重要应用场景
- 常见问题及解决方案
- 技术优缺点对比

### ⭐ 补充内容（简要提及）
- 历史背景
- 非核心技术细节
- 了解即可的内容

## 二、内容组织方式

```markdown
## 第X章 章节名称

### ⭐⭐⭐ 核心必考知识

#### 【概念】知识点名称
**定义**: （精确定义，适合名词解释）  
**核心要点**: （3-5个要点）  
**常见题型**: 名词解释/简答题/选择题  
**答题要点**: （如何组织答案）

#### 【公式】公式名称
**公式**: $$公式内容$$  
**参数说明**: （每个参数的含义）  
**应用场景**: （何时使用）  
**计算示例**: （简单示例）  
**常见陷阱**: （易错点）

#### 【对比】A vs B
| 对比维度 | A | B |
|---------|---|---|
| 核心特征 | ... | ... |
| 典型应用 | ... | ... |

**记忆要点**: （核心区别，不用类比口诀）  
**题型**: 选择题/判断题/简答题

#### 【流程】协议/算法名称
**步骤**:  
1. 步骤1: 详细说明  
2. 步骤2: 详细说明  
...

**关键点**: （必须掌握的环节）  
**常考点**: （考试重点）

### ⭐⭐ 重要知识

- **知识点A**: 简要说明（1-2句话）  
- **知识点B**: 简要说明

### ⭐ 补充内容

- 背景知识: 一句话概括

### 💡 本章考试重点

1. **必背概念**: 列出3-5个核心概念  
2. **必会计算**: 列出2-3类计算题  
3. **必懂流程**: 列出1-2个关键流程  
4. **易混淆点**: 列出2-3组对比  
5. **历年真题方向**: （如果有）

### 🔗 章节关联

- **前置知识**: 需要先掌握哪些章节  
- **后续应用**: 本章为哪些章节打基础  
- **跨章综合**: 可能与哪些章节结合出题
```

## 三、内容要求

### 3.1 概念定义
- 必须准确、完整、规范
- 适合直接作为名词解释答案
- 包含关键词和核心特征
- 长度控制在3-5句话

### 3.2 公式处理
- 使用LaTeX格式（$$公式$$）
- 明确说明每个参数含义
- 提供计算示例
- 标注单位和取值范围
- 指出易错点

### 3.3 对比辨析
- 使用表格形式
- 对比维度明确
- 突出核心差异
- 指出选择题常考点

### 3.4 流程步骤
- 分步骤清晰列出
- 每步说明要点
- 标注关键环节
- 指出简答题答题框架

### 3.5 题型标注
每个核心知识点必须标注：
- 📝 名词解释
- ✏️ 填空题
- ✅ 选择题/判断题
- 📊 计算题
- 📄 简答题
- 🔄 综合题

## 四、禁止事项

❌ **不要使用**:
1. 类比记忆（如"TCP像打电话"）
2. 口诀记忆（如"唐僧到西天取经"）
3. 生活化比喻
4. 过度展开的非核心内容
5. 娱乐化表述

✅ **应该做**:
1. 使用学术化、规范化表述
2. 突出逻辑关系和因果关系
3. 强调知识点之间的对比
4. 提供答题框架和要点
5. 标注题型和考点

## 五、质量标准

1. **准确性**: 概念定义准确无误
2. **完整性**: 核心知识点无遗漏
3. **应试性**: 突出题型和答题要点
4. **结构性**: 层次清晰，便于记忆
5. **精简性**: 去除冗余，聚焦考点

## 六、特别要求

1. **对于每个⭐⭐⭐知识点**:
   - 必须说明常见题型
   - 必须提供答题要点
   - 如有公式，必须给出计算示例

2. **对于易混淆的概念**:
   - 必须使用对比表格
   - 必须指出核心区别
   - 必须标注选择题考点

3. **对于协议和算法**:
   - 必须列出清晰步骤
   - 必须标注关键环节
   - 必须说明简答题答题框架

4. **章末总结**:
   - 必须列出必背概念
   - 必须列出必会计算
   - 必须总结易混淆点

请开始生成应试导向的复习笔记："""
    
    @staticmethod
    def _get_v2_prompt() -> str:
        """v2.0提示词 - 重要性分级版"""
        return """请基于这份课件PDF，生成一份高质量的复习笔记。

**核心要求：按知识点重要性分级处理**

1. **重要性评估标准**：
   - ⭐⭐⭐ 高重要性：核心概念、基础理论、必考知识点、关键公式
   - ⭐⭐ 中重要性：重要应用、典型例子、常见问题
   - ⭐ 低重要性：补充说明、历史背景、非核心内容

2. **内容组织方式**（按重要性排序）：
   ```markdown
   ## 章节名称
   
   ### ⭐⭐⭐ 核心知识点（必须掌握）
   - **概念A**：详细定义和说明（展开2-3句）
   - **概念B**：详细定义和说明
   - **重要公式**：公式 + 详细含义 + 应用场景
   
   ### ⭐⭐ 重要知识点（需要理解）
   - 知识点C：简要说明（1-2句）
   - 知识点D：简要说明
   
   ### ⭐ 补充内容（了解即可）
   - 背景知识：一句话概括
   
   ### 💡 本章重点总结
   用3-5个要点列出本章最重要的内容。
   
   ### 🔗 知识关联
   说明本章与其他章节的关联关系（如果有）。
   ```

3. **重点知识点处理原则**：
   - ⭐⭐⭐ 级别：详细展开，包括定义、原理、公式、应用
   - ⭐⭐ 级别：适度展开，说明要点和用途
   - ⭐ 级别：一句话带过，或直接省略

4. **必须识别的高重要性内容**：
   - 协议名称和工作原理
   - 关键算法和公式
   - 网络层次和架构
   - 核心概念的定义
   - 典型问题和解决方案

5. **格式要求**：
   - 使用 ⭐ 符号标注重要性级别
   - 高重要性内容用 **粗体** + 详细说明
   - 中重要性用普通格式 + 简要说明
   - 低重要性可省略或一句话
   - 每章必须有"本章重点总结"和"知识关联"

6. **思维导图素材**：
   - 在笔记末尾，单独列出"思维导图关键节点"
   - 列出章节、核心概念、关键关系
   - 为后续生成思维导图提供素材

**注意事项**：
- 重点内容宁多勿少，次要内容可大胆省略
- 概念定义必须准确，不能为了简洁而牺牲准确性
- 考试重点用 🎯 标注
- 实践应用用 💻 标注

请开始生成笔记："""
